# numerical operatoins on list elements

numbers = [10,4,5.5,7,1]

# smallest number in the list
# min()

print(min(numbers))
print (f"Smallest number: {min(numbers)} ")

# biggest number in the list
# max

print(max(numbers))
print (f"Biggest number: {max(numbers)}")

# Total of the numbers in the list
# sum()

print(sum(numbers))
print(f"Total number of the list: {sum(numbers)}")